    private void exibeInformacoes() {
        ArrayList<Cliente> clien = clientes.retornaTodos();

        //limpar as colunas
        for (int i = 0; i < clien.size(); i++) {
            tDetalhe.setValueAt("", i, 0);
            tDetalhe.setValueAt("", i, 1);
            tDetalhe.setValueAt("", i, 2);
            tDetalhe.setValueAt("", i, 3);

        }
        for (int i = 0; i < clien.size() && clien.get(i) != null; i++) {
            tDetalhe.setValueAt(clien.get(i).getNome(), i, 0);
            tDetalhe.setValueAt(clien.get(i).getSexo(), i, 1);
            tDetalhe.setValueAt(clien.get(i).getAltura(), i, 2);
            tDetalhe.setValueAt(clien.get(i).getPeso(), i, 3);
        }
    }
